STAK    SEGMENT PARA STACK 'STACK'
        DW 20 DUP(?)
STAK    ENDS

DATA    SEGMENT PARA 'DATA'
DIGITS  DB 00H
DATA    ENDS

CODE    SEGMENT PARA 'CODE'
        ASSUME CS:CODE, DS:DATA, SS:STAK
START PROC
        MOV AX, DATA
	MOV DS, AX
	

	mov DX, 1000H  
	MOV AL, 00H

ENDLESS:	
	OUT DX, AL
DELAY1:
MOV CX, 02H
L1:

LOOP L1	
	INC AL
	CMP AL, 0FFH
	JE IKI
        JMP ENDLESS
IKI:	
	OUT DX, AL
DELAY2:
MOV CX, 02H
L2:

LOOP L2	
	DEC AL
	CMP AL, 00H
	JE ENDLESS
        JMP IKI	
	
	RET
START ENDP	

	
CODE    ENDS
        END START
